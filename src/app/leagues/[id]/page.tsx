//******************************************************************************
// imports
//******************************************************************************
import styles        from "./page.module.css"
import { getLeague } from "@/lib/league";


//******************************************************************************
// types
//******************************************************************************
type Props = {
  params : {
    id : string ,
  }
}


//******************************************************************************
// LeaguePage
//******************************************************************************
const LeaguePage = async ( { params : { id } } : Props ) => {
  const league = await getLeague( id );

  return <main className={ styles.main }>
    <h2>{ league?.name || "" }</h2>


  </main>
};


//******************************************************************************
// exports
//******************************************************************************
export default LeaguePage;
