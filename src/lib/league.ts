//******************************************************************************
// import
//******************************************************************************
import { prisma } from "./db";


//******************************************************************************
// create
//******************************************************************************
async function createLeague( name : string ) {
  "use server"
  return await prisma.league.create({
    data : { name } ,
  });
};


//******************************************************************************
// get
//******************************************************************************
async function getLeague( id : string ) {
  "use server"
  return await prisma.league.findFirst({ where : { id } });
};


//******************************************************************************
// getAllLeagues
//******************************************************************************
async function getAllLeagues() {
  "use server"
  return await prisma.league.findMany();
};


//******************************************************************************
// update
//******************************************************************************
async function updateLeague( id : string, name : string ) {
  "use server"
  return await prisma.league.update({
    where : { id   } ,
    data  : { name } ,
  });
};


//******************************************************************************
// delete
//******************************************************************************
async function deleteLeague( id : string ) {
  "use server"
  return await prisma.league.delete({
    where : { id } ,
  });
};


//******************************************************************************
// export
//******************************************************************************
export { createLeague, getLeague, getAllLeagues, updateLeague, deleteLeague };
